#LAMBDA FUNC

#add 2 numbers
add = lambda a, b: a + b
print(add(2, 3))

#Square of numbers
square = lambda x: x * x
print(square(4))

#cubes
cube = lambda x: x ** 3
print(cube(3))

#even and odd
evenodd = lambda x: "Even" if x % 2 == 0 else "Odd"
print(evenodd(5))

#max of 2 numbers
max_val = lambda a, b: a if a > b else b
print(max_val(10, 20))

