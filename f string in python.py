#f STRING

name="Vishnu"
age=18
print(f"My name is {name} and I am {age} years old")#by f' string

print("My name is {} and I'm {} years old".format(name,age))# by using format

print("My name is {0} and I'm {1} years old".format(name,age))

print("My name is {n} and I'm {a} years old".format(n=name,a=age))

print("My name is %s and I'm %d years old" %(name,age))#by using format specifier
